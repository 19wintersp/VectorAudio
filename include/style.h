inline static void push_disabled_on(bool flag)
{
    if (!flag) return;

    ImGui::PushItemFlag(ImGuiItemFlags_Disabled, true);
    ImGui::PushStyleVar(ImGuiStyleVar_Alpha, ImGui::GetStyle().Alpha * 0.5);
};

inline static void pop_disabled_on(bool flag)
{
    if (!flag) return;

    ImGui::PopItemFlag();
    ImGui::PopStyleVar();
};

